package sec07;

public class SwitchExam {

	public static void main(String[] args) {
		int val = 2;
		switch (val) {
		case 1:
			System.out.println("value : 1");
			break;
		case 2:
			System.out.println("value : 2");
			break;
		
		case 3:
			System.out.println("value : 3");
			break;
		default:
			System.out.println("vlaue : not1~3");
			break;
		}
		System.out.println("End");
	}

}
